# Ruby Variable Naming Conventions (a-z, underscores)

# Valid Variable Names

my_age
last_name
favorite_foods


# Invalid Variable Names

MyAge
Last Name
favorite foods


# Variable Assignment
# Use one equals sign (=) to assign a value to a variable

my_age = 28
last_name = "Warbelow"
favorite_foods = ["Cheerios", "pasta", "chocolate"]
